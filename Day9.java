public class Day9 {
	public static void main(String[] args) {
//deklarasi
		String nama;
		int umur;
//artinya kita membuat variabel baru dengan tipe data tertentu, tanpa harus langsung memberi nilai

//inisialisasi	
		nama = "Cahyo Susanto Basuki";
		umur = 18;
//artinya memberi nilai pertama kali pada variabel. bisa dilakukan langsung saat deklarasi atau setelahnya
		
//hasil inisialisasi
        System.out.println("Inisialisasi");
		System.out.println("Nama: "+ nama);
		System.out.println("Umur: "+ umur);
		
//update variabel
		nama = "@chyoo1000";
		umur = 18; //menolak tua:v
//artinya mengubah nilai variabel yang sudah ada

//hasil setelah update
        System.out.println("\nUpdate Variabel");		
		System.out.println("Nama: "+ nama);
		System.out.println("Umur: "+ umur);
	}
}
